function GetFill(monthData: iWaterDataPerMonthAndDay, day: number) {
	var waterTotalValue = monthData.perday[day] && monthData.perday[day].totalWater;
	if (!waterTotalValue) return '#d3e3df';
	if (waterTotalValue >= 0 && waterTotalValue <= 1000) return '#00f2f8';
	if (waterTotalValue >= 1000 && waterTotalValue <= 2000) return '#00c3ff';
	if (waterTotalValue >= 2000 && waterTotalValue <= 3000) return '#0093fb';
	if (waterTotalValue > 3000) return '#0032fb';
}

